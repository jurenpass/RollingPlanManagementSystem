# -*- coding: utf-8 -*-
内容 = '1000表面热镀外板：只投单道次除鳞。'

def 计算显示宽度(文本):
    宽度 = 0
    for 字符 in 文本:
        if 字符.isascii():
            宽度 += 0.6
        else:
            宽度 += 1
    return 宽度

总宽度 = 计算显示宽度(内容)
print(f'内容: {内容}')
print(f'总显示宽度: {总宽度:.2f}')
print(f'字符数: {len(内容)}')
print()

# 实际显示2行
print('测试不同每行宽度:')
for 每行宽度 in [11, 12, 13, 14, 15, 16, 17]:
    需要行数 = max(1, int((总宽度 + 每行宽度 - 1) // 每行宽度))
    需要行数_加安全 = 需要行数 + 1
    print(f'每行宽度={每行宽度}: 基础行数={需要行数}, 含安全={需要行数_加安全}')

print()
print('目标: 实际显示2行，计算应该也是2行（或含安全后不超过3行）')
print()

# 模拟实际显示
print('模拟实际显示(每行宽度=17):')
当前行宽度 = 0
当前行内容 = ''
行数 = 0
for 字符 in 内容:
    if 字符.isascii():
        字符宽度 = 0.6
    else:
        字符宽度 = 1
    
    if 当前行宽度 + 字符宽度 > 17:
        行数 += 1
        print(f'第{行数}行: {当前行内容}')
        当前行内容 = 字符
        当前行宽度 = 字符宽度
    else:
        当前行内容 += 字符
        当前行宽度 += 字符宽度

if 当前行内容:
    行数 += 1
    print(f'第{行数}行: {当前行内容}')

print(f'\n实际显示行数: {行数}')
