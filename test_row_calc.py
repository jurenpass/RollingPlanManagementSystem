# -*- coding: utf-8 -*-
内容 = '全流程，980≤RT2上表均值≤1010，1000≤RT2下表均值≤1030，RT1辊速0.8，全除鳞（严禁使用增强除鳞），5台工频+5道次+末两道次限速4.5（不允许手动改7道次生产），R2在机天数≤5天。'

def 计算显示宽度(文本):
    宽度 = 0
    for 字符 in 文本:
        if 字符.isascii():
            宽度 += 0.6  # 英文字符和数字占0.6个中文宽度
        else:
            宽度 += 1  # 中文字符占1个宽度
    return 宽度

总宽度 = 计算显示宽度(内容)
每行宽度 = 17  # 每行约17个中文宽度
需要行数 = max(1, int((总宽度 + 每行宽度 - 1) // 每行宽度))
需要行数_加安全 = 需要行数 + 1

print(f'内容长度: {len(内容)} 字符')
print(f'总显示宽度: {总宽度:.2f}')
print(f'计算行数(不含安全): {需要行数}')
print(f'计算行数(含安全+1): {需要行数_加安全}')
print()
print('逐行分析:')

# 模拟Excel换行
当前行宽度 = 0
当前行内容 = ''
行数 = 0
for 字符 in 内容:
    if 字符.isascii():
        字符宽度 = 0.6
    else:
        字符宽度 = 1
    
    if 当前行宽度 + 字符宽度 > 17:
        行数 += 1
        print(f'第{行数}行: {当前行内容}')
        当前行内容 = 字符
        当前行宽度 = 字符宽度
    else:
        当前行内容 += 字符
        当前行宽度 += 字符宽度

if 当前行内容:
    行数 += 1
    print(f'第{行数}行: {当前行内容}')

print(f'\n实际显示行数: {行数}')
print(f'代码计算行数(含安全): {需要行数_加安全}')
print(f'行高应该是: {500 + (需要行数_加安全 - 1) * 360} twips ({25 + (需要行数_加安全 - 1) * 18}pt)')
