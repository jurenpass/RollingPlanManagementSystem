# -*- coding: utf-8 -*-

def 计算显示宽度(文本):
    宽度 = 0
    for 字符 in 文本:
        if 字符.isascii():
            宽度 += 0.6
        else:
            宽度 += 1
    return 宽度

def 计算行数(内容, 每行宽度=13):
    总宽度 = 计算显示宽度(内容)
    需要行数 = max(1, int((总宽度 + 每行宽度 - 1) // 每行宽度))
    
    # 新逻辑：只有最后一行超过80%宽度时才增加1行
    最后一行宽度 = 总宽度 % 每行宽度
    if 最后一行宽度 == 0:
        最后一行宽度 = 每行宽度
    if 最后一行宽度 > 每行宽度 * 0.8:
        需要行数 = 需要行数 + 1
    
    return 需要行数, 总宽度, 最后一行宽度

# 测试用例
test_cases = [
    ('1000表面热镀外板：只投单道次除鳞。', 2),  # 实际2行
    ('全流程，980≤RT2上表均值≤1010，1000≤RT2下表均值≤1030，RT1辊速0.8，全除鳞（严禁使用增强除鳞），5台工频+5道次+末两道次限速4.5（不允许手动改7道次生产），R2在机天数≤5天。', 8),  # 实际8行
]

print('测试新行数计算逻辑:')
print('=' * 80)

for 内容, 预期行数 in test_cases:
    需要行数, 总宽度, 最后一行宽度 = 计算行数(内容)
    行高 = 500 + (需要行数 - 1) * 360
    
    print(f'\n内容: {内容[:30]}...' if len(内容) > 30 else f'\n内容: {内容}')
    print(f'  总宽度: {总宽度:.2f}')
    print(f'  最后一行宽度: {最后一行宽度:.2f} (阈值: {13 * 0.8:.2f})')
    print(f'  计算行数: {需要行数}')
    print(f'  预期行数: {预期行数}')
    print(f'  行高: {行高} twips ({25 + (需要行数 - 1) * 18}pt)')
    print(f'  结果: {"✓" if 需要行数 == 预期行数 else "✗"}')
