内容 = '全流程，980≤RT2上表均值≤1010，1000≤RT2下表均值≤1030，RT1辊速0.8，全除鳞（严禁使用增强除鳞），5台工频+5道次+末两道次限速4.5（不允许手动改7道次生产），R2在机天数≤5天。'

def 计算显示宽度(文本):
    宽度 = 0
    for 字符 in 文本:
        if 字符.isascii():
            宽度 += 0.6  # 英文字符和数字占0.6个中文宽度
        else:
            宽度 += 1  # 中文字符占1个宽度
    return 宽度

总宽度 = 计算显示宽度(内容)

print('测试不同每行宽度参数:')
print('=' * 60)

for 每行宽度 in [15, 16, 17]:
    需要行数 = max(1, int((总宽度 + 每行宽度 - 1) // 每行宽度))
    需要行数_加安全 = 需要行数 + 1
    行高 = 500 + (需要行数_加安全 - 1) * 360
    
    print(f'\n每行宽度 = {每行宽度}:')
    print(f'  计算行数(不含安全): {需要行数}')
    print(f'  计算行数(含安全+1): {需要行数_加安全}')
    print(f'  行高: {行高} twips ({25 + (需要行数_加安全 - 1) * 18}pt)')

print('\n' + '=' * 60)
print('目标: 8行显示 -> 行高应该是 500 + (8-1)*360 = 3020 twips (151pt)')
